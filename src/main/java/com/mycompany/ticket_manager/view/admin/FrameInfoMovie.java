    private void clickStopMovie(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_clickStopMovie
        // TODO add your handling code here:
        if (this.id.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Id trống", "Thông báo lỗi", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (this.stop.isSelected()) {
            JOptionPane.showMessageDialog(null, "Không thể dừng chiếu", "Thông báo lỗi", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Response<List<Object>> response = this.movieController.stopMovie(this.id.getText());

        if (response.getSuccess() == false) {
            JOptionPane.showMessageDialog(null, response.getMessage(), "Thông báo lỗi", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Movie movieInList = this.listMovie.get(table.getSelectedRow());
        movieInList.setHideAt((Long) response.getData().get(1));
        movieInList.setStrHideAt(response.getData().get(0).toString());
        this.stop.setSelected(true);
        this.stop.setText(movieInList.getStrHideAt());
        this.pushTable();
    }//GEN-LAST:event_clickStopMovie

    private void clickRowTable(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_clickRowTable
        // TODO add your handling code here:
        this.clean();
        int row = this.table.getSelectedRow();
        this.showInfoMovie(this.listMovie.get(row));

    }//GEN-LAST:event_clickRowTable

    private void clickEdit(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clickEdit
        // TODO add your handling code here:
        Movie movie = new Movie();
        movie.setId(this.id.getText());
        movie.setName(this.name.getText());
        movie.setAge(this.getAge());
        movie.setMinPrice((int) this.price.getValue());
        movie.setTime((int) this.time.getValue());

        Response<Movie> response = this.movieController.editMovie(movie);
        if (response.getSuccess() == false) {
            JOptionPane.showMessageDialog(null, response.getMessage(), "Thông báo lỗi", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Movie movieInList = this.listMovie.get(table.getSelectedRow());
        movieInList.setName(response.getData().getName());
        movieInList.setAge(response.getData().getAge());
        movieInList.setMinPrice(response.getData().getMinPrice());
        movieInList.setTime(response.getData().getTime());
        this.pushTable();
        JOptionPane.showMessageDialog(null, "Cập nhật thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_clickEdit

    private void clickFindMovie(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clickFindMovie
        // TODO add your handling code here:
        Response<List<Movie>> response = this.movieController.findMovie(this.find_id.getText(), this.find_name.getText());
        if (response.getSuccess() == false) {
            JOptionPane.showMessageDialog(null, response.getMessage(), "Thông báo lỗi", JOptionPane.WARNING_MESSAGE);
            return;
        }
        this.clean();
        this.listMovie = response.getData();
        this.pushTable();
    }//GEN-LAST:event_clickFindMovie

