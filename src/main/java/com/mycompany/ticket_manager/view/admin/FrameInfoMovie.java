    private void ftMovie(boolean playing) {
        Response<List<Movie>> response = this.movieController.findMovieStatus(playing);
        this.clean();
        this.listMovie = response.getData();
        this.pushTable();
    }

    private int getAge() {
        if (this.child.isSelected()) {
            return 1;
        } else if (this.middle.isSelected()) {
            return 16;
        } else if (this.old.isSelected()) {
            return 18;
        } else {
            return -1;
        }
    }

    private void loadAllStaffToTable() {
        Response<List<Movie>> response = this.movieController.getAllMovie();

        if (response.getSuccess() == false) {
            JOptionPane.showMessageDialog(null, response.getMessage(), "Thông báo lỗi", JOptionPane.WARNING_MESSAGE);
            return;
        }
        this.listMovie = response.getData();
        this.pushTable();
    }

    private void pushTable() {
        DefaultTableModel tableModel = (DefaultTableModel) table.getModel();
        tableModel.setRowCount(0);
        for (Movie movie : this.listMovie) {
            Object[] data = {movie.getId(), movie.getName(), movie.getAge(), movie.getMinPrice(), movie.getTime(), movie.getHideAt() == 0 ? "Đang chiếu" : "Dừng chiếu"};
            tableModel.addRow(data);
        }
    }

    private void clean() {
        this.name.setText("");
        this.id.setText("");
        this.nocheck.setSelected(true);
        this.price.setValue(0);
        this.time.setValue(0);
        this.stop.setText("");
        this.stop.setSelected(false);
        this.create.setText("");
    }

    private void showInfoMovie(Movie movie) {
        this.id.setText(movie.getId());
        this.name.setText(movie.getName());
        this.setAge(movie.getAge());
        this.price.setValue(movie.getMinPrice());
        this.time.setValue(movie.getTime());
        if (movie.getHideAt() != 0) {
            this.stop.setSelected(true);
            this.stop.setText(movie.getStrHideAt());
        }
        this.create.setText(movie.getStrCreateAt());
    }

    private void setAge(int age) {
        switch (age) {
            case 1:
                this.child.setSelected(true);
                break;
            case 16:
                this.middle.setSelected(true);
                break;
            case 18:
                this.old.setSelected(true);
                break;
            default:
                this.nocheck.setSelected(true);
                break;
        }
    }


